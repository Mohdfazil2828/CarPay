package com.parkingapp.activity.activities.loginmodal;

public class UserLoginModal {

    private String enter_exit_ID;
    private String user_Id;
    private String admin_Id;
    private String time_in;
    private String time_out;
    private String amount;
    private String time_flag;


    public UserLoginModal() {
    }

    public String getEnter_exit_ID() {
        return enter_exit_ID;
    }

    public void setEnter_exit_ID(String enter_exit_ID) {
        this.enter_exit_ID = enter_exit_ID;
    }

    public String getUser_Id() {
        return user_Id;
    }

    public void setUser_Id(String user_Id) {
        this.user_Id = user_Id;
    }

    public String getAdmin_Id() {
        return admin_Id;
    }

    public void setAdmin_Id(String admin_Id) {
        this.admin_Id = admin_Id;
    }

    public String getTime_in() {
        return time_in;
    }

    public void setTime_in(String time_in) {
        this.time_in = time_in;
    }

    public String getTime_out() {
        return time_out;
    }

    public void setTime_out(String time_out) {
        this.time_out = time_out;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getTime_flag() {
        return time_flag;
    }

    public void setTime_flag(String time_flag) {
        this.time_flag = time_flag;
    }
}
